package tv1;

public class LgTv {
	public void powerOn() {
		System.out.println("LG TV - 전원을 켠다");
	}
	public void powerOff() {
		System.out.println("LG TV - 전원을 끈다");
	}
	public void volumUp() {
		System.out.println("LG TV - 소리를 높인다");
	}	
	public void volumDown() {
		System.out.println("LG TV - 소리를 줄인다");	
	}
}
